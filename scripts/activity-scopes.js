function sectionNumber(code) {
  const value = Number(String(code || '').split('/').at(-1));
  return Number.isInteger(value) ? value : null;
}

function activityScope(row) {
  return ACTIVITY_SCOPES[row.sector]?.[sectionNumber(row.code)] || null;
}

